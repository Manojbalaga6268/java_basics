package com.switchcase;

public class SwitchExample {

	public static void main(String[] args) {

		int x = 20;

		switch (x) {

		case 10:
			System.out.println("It is ten");
			break;
		case 20:
			System.out.println("it is twenty");
			break;
		default:
			System.out.println("It doesnt match with any thing");
		}
	}

}
